mpn_t mpn;
mnp_t mnp;

void schema_name_init (nitpick& nits)
{   assert (mpn.empty ());
    assert (mnp.empty ());
    for (property_name* p = &namtab [0]; p -> prop_ != sp_illegal; ++p)
        if (p -> name_ != nullptr)
        {   ::std::string n (::boost::to_lower_copy (::std::string (p -> name_)));
            if (mpn.find (n) != mpn.cend ())
                nits.pick (nit_symbol_aleady_defined, es_catastrophic, ec_init, "schema property name ", n, " defined more than once");
            else if (mnp.find (p -> prop_) != mnp.cend ())
            {   nits.pick (nit_symbol_aleady_defined, es_debug, ec_init, "schema property ", p -> prop_, " given more than one name");
                mpn.insert (mpn_t::value_type (n, p -> prop_)); }
            else
            {   mpn.insert (mpn_t::value_type (n, p -> prop_));
                mnp.insert (mnp_t::value_type (p -> prop_, p -> name_)); } } }

::std::string schema_property_name (const e_schema_property p)
{   mnp_t::const_iterator i = mnp.find (p);
    if (i == mnp.cend ()) return ::std::string ();
    return i -> second; }

e_schema_property get_schema_property (const ::std::string& s)
{   mpn_t::const_iterator i = mpn.find (s);
    if (i == mpn.cend ()) return sp_illegal;
    return i -> second; }
